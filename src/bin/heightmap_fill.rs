fn volume_difference(a: &Vec<Vec<i32>>, b: &Vec<Vec<i32>>) -> usize {
    let mut diff = 0;
    for (row1, row2) in a.iter().zip(b.iter()) {
        for (cell1, cell2) in row1.iter().zip(row2) {
            diff += (*cell1 - *cell2).abs() as usize;
        }
    }
    diff
}

fn volume(heightmap: &Vec<Vec<i32>>) -> usize {
    let debug = false;
    let debug2 = true;

    let mut swap = heightmap.clone();
    let height = heightmap.len();
    let width = heightmap[0].len();
    let is_edge = |i: usize, j: usize| i == 0 || j == 0 || i + 1 == width || j + 1 == height;

    let minimal_value = *heightmap.iter().flatten().min().unwrap();
    let maximal_value = *heightmap.iter().flatten().max().unwrap();
    let diff = maximal_value - minimal_value;

    if debug {
        let mut printy = String::new();
        for row in &swap {
            printy.push_str(format!("{:?}\n", row).as_str());
        }
        println!("{}", printy);
    }

    for row in swap.iter_mut() {
        for cell in row.iter_mut() {
            *cell += diff;
        }
    }
    let mut window = Window::new(
        "Swarm",
        width,
        height,
        WindowOptions {
            scale: Scale::X2,
            ..WindowOptions::default()
        },
    )
    .unwrap_or_else(|e| {
        panic!("{}", e);
    });
    let mut buffer = vec![0u32; width * height];

    for (y, row) in heightmap.iter().enumerate() {
        for (x, cell) in row.iter().enumerate() {
            let f = ((*cell - minimal_value) as f32 / (diff) as f32).min(1.0);
            buffer[y * width + x] =
                rgb_to_u32((f * 255.0) as u8, (f * 255.0) as u8, (f * 255.0) as u8);
        }
    }

    // Limit to max ~144 fps update rate
    window.limit_update_rate(Some(std::time::Duration::from_micros(6944)));

    let mut step_count = 0;

    if debug && width < 30 {
        let mut printy = String::new();
        for row in &swap {
            printy.push_str(format!("{:?}\n", row).as_str());
        }
        println!("{}", printy);
    }

    if width == 1 || height == 1 {
        return 0;
    }
    let width_1 = width - 1;
    let height_1 = height - 1;

    let mut tracked_tiles = Vec::new();
    // drain edges
    for i in 0..width {
        for j in [0, height - 1] {
            if is_edge(i, j) {
                swap[j][i] = heightmap[j][i];
                tracked_tiles.push((i, j));
            }
        }
    }
    for j in 0..height {
        for i in [0, width - 1] {
            if is_edge(i, j) {
                swap[j][i] = heightmap[j][i];
                tracked_tiles.push((i, j));
            }
        }
    }

    tracked_tiles.sort_by_key(|e| e.0 + e.1 * width);
    loop {
        let tile = tracked_tiles.pop();

        if let Some((i, j)) = tile {
            let tile_value = swap[j][i];
            if debug {
                println!(
                    "checking tile index {} {} with tile value {}",
                    i, j, tile_value
                );
            }
            // check neighbors
            if (i, j) == (0, 0)
                || (i, j) == (0, height_1)
                || (i, j) == (width_1, 0)
                || (i, j) == (width_1, height_1)
            {
                // corner tiles can be ignored, since all edges were already tracked and drained
                continue;
            }
            if i == 0 {
                // left edge
                // thus only the tile to the right should be checked
                if swap[j][i + 1] > tile_value && swap[j][i + 1] > heightmap[j][i + 1] {
                    let k = (i + 1, j);
                    if let Err(insertion_idx) = tracked_tiles
                        .binary_search_by_key(&(k.0 + k.1 * width), |e| e.0 + e.1 * width)
                    {
                        tracked_tiles.insert(insertion_idx, k);
                    }
                }
                continue;
            }
            if i == width_1 {
                // right edge
                // thus only the tile to the left should be checked
                if swap[j][i - 1] > tile_value && swap[j][i - 1] > heightmap[j][i - 1] {
                    let k = (i - 1, j);
                    if let Err(insertion_idx) = tracked_tiles
                        .binary_search_by_key(&(k.0 + k.1 * width), |e| e.0 + e.1 * width)
                    {
                        tracked_tiles.insert(insertion_idx, k);
                    }
                }
                continue;
            }
            if j == 0 {
                // top edge
                // thus only the tile below should be checked
                if swap[j + 1][i] > tile_value && swap[j + 1][i] > heightmap[j + 1][i] {
                    let k = (i, j + 1);
                    if let Err(insertion_idx) = tracked_tiles
                        .binary_search_by_key(&(k.0 + k.1 * width), |e| e.0 + e.1 * width)
                    {
                        tracked_tiles.insert(insertion_idx, k);
                    }
                }
                continue;
            }
            if j == height_1 {
                // bottom edge
                // thus only the tile above should be checked
                if swap[j - 1][i] > tile_value && swap[j - 1][i] > heightmap[j - 1][i] {
                    let k = (i, j - 1);
                    if let Err(insertion_idx) = tracked_tiles
                        .binary_search_by_key(&(k.0 + k.1 * width), |e| e.0 + e.1 * width)
                    {
                        tracked_tiles.insert(insertion_idx, k);
                    }
                }
                continue;
            }
            let mut drained = false;
            let cardinal_neighbors = [(i + 1, j), (i - 1, j), (i, j + 1), (i, j - 1)];

            if swap[j][i] > heightmap[j][i] {
                // if current tile is drainable.
                for (new_i, new_j) in &cardinal_neighbors {
                    if debug {
                        println!("{} {}: {}", new_i, new_j, swap[*new_j][*new_i]);
                    }
                    if swap[*new_j][*new_i] < swap[j][i] {
                        swap[j][i] = swap[*new_j][*new_i].max(heightmap[j][i]);
                        if debug {
                            println!("new tile value: {}", swap[j][i]);
                        }
                        if swap[j][i] - tile_value != 0 {
                            drained = true;
                        }
                    }
                }
            }
            // let tile_value = swap[j][i];
            if drained {
                // current tile drained.
                if debug {
                    println!(
                    "tile drained, checking neighbors for drainable tiles greater than current tile"
                );
                }
                for (ni, nj) in &cardinal_neighbors {
                    let k = (*ni, *nj);
                    if swap[k.1][k.0] > swap[j][i] && swap[k.1][k.0] > heightmap[k.1][k.0] {
                        // if neighbor tile is larger than this tile and has drainable water, check that tile next
                        if let Err(insertion_idx) = tracked_tiles
                            .binary_search_by_key(&(k.0 + k.1 * width), |e| e.0 + e.1 * width)
                        {
                            if debug {
                                println!("inserting {} {}", k.0, k.1);
                            }
                            tracked_tiles.insert(insertion_idx, k);
                        }
                    }
                }
                let f = ((swap[j][i] - minimal_value) as f32 / (diff) as f32).min(1.0);
                buffer[j * width + i] =
                    rgb_to_u32((f * 255.0) as u8, (f * 255.0) as u8, (f * 255.0) as u8);
            }
        } else {
            // no more tracked tiles
            break;
        }
        step_count += 1;

        if debug && width < 30 {
            let mut printy = String::new();
            for row in &swap {
                printy.push_str(format!("{:?}\n", row).as_str());
            }
            println!("{}", printy);
        }
        if step_count % 100 == 0 {
            window.update_with_buffer(&buffer, width, height).unwrap();
        }
        // if debug2 {
        //     if step_count % 1000 == 0 {
        //         print!("{:?} ", tracked_tiles.len());
        //         println!("{}", step_count);
        //     }
        // }
    }

    if debug {
        let mut printy = String::new();
        for row in &swap {
            printy.push_str(format!("{:?}\n", row).as_str());
        }
        println!("{}", printy);
    }

    if debug2 {
        println!("steps: {}", step_count);
    }
    // window.
    // drop(window);
    volume_difference(&swap, heightmap)
}

fn main() {
    use rand::random;
    for i in 100..1000 {
        let width = i;
        let height = i;
        let mut map = Vec::new();
        for _ in 0..height {
            let mut row = Vec::new();
            for _ in 0..width {
                row.push(((random::<f32>() - 0.5) * 100.0) as i32);
            }
            map.push(row);
        }
        print!("{}, ", width * height);
        volume(&map);
    }
}

